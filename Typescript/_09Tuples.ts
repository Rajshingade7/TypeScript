// Tuple to represent employee details: id, name, age
let employee: [number, string, number] = [1, "John Doe", 30];

// Accessing tuple elements
console.log(employee[0]); // Output: 1
console.log(employee[1]); // Output: John Doe
console.log(employee[2]); // Output: 30

// Tuple to represent coordinates: x, y
let point: [number, number] = [10, 20];

// Accessing tuple elements
console.log(point[0]); // Output: 10
console.log(point[1]); // Output: 20

